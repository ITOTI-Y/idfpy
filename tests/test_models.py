def test_idf_all_of_type():
    from idfpy import IDF
    from idfpy.models import Zone

    idf = IDF()
    idf.add(Zone(name='Z1'))
    idf.add(Zone(name='Z2'))

    zones = idf.all_of_type('Zone')
    assert len(zones) == 2
    assert 'Z1' in zones
    assert 'Z2' in zones


