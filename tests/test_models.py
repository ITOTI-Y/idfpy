def test_idf_save_and_load(tmp_path):
    from idfpy import IDF
    from idfpy.models import Building, Version, Zone

    idf = IDF()
    idf.add(Version())
    idf.add(Building(north_axis=0.0))
    idf.add(Zone(name='Office'))

    path = tmp_path / 'test.idf'
    idf.save(path)

    assert path.exists()
    content = path.read_text()
    assert 'Generated by idfpy' in content
    assert 'Zone' in content
    assert 'Office' in content

    loaded = IDF.load(path)
    assert len(loaded) >= 2
    assert loaded.has('Zone', 'Office')


